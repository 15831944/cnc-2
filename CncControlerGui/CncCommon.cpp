#include "CncCommon.h"

//////////////////////////////////////////////////////////////
bool cnc::dblCompare( double a, double b, double eps) {
//////////////////////////////////////////////////////////////
	return std::abs(a - b) <= eps; 
}
//////////////////////////////////////////////////////////////
bool cnc::dblCompareNull( double a, double eps) {
//////////////////////////////////////////////////////////////
	return cnc::dblCompare(a, 0.0, eps);
	
}