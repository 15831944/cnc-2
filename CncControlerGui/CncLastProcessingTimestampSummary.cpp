#include "CncLastProcessingTimestampSummary.h"

/////////////////////////////////////////////////////////////
CncLastProcessingTimestampSummary::CncLastProcessingTimestampSummary(wxWindow* parent)
: CncLastProcessingTimestampSummaryBase(parent)
/////////////////////////////////////////////////////////////
{
}
/////////////////////////////////////////////////////////////
CncLastProcessingTimestampSummary::~CncLastProcessingTimestampSummary() {
/////////////////////////////////////////////////////////////
}
/////////////////////////////////////////////////////////////
void CncLastProcessingTimestampSummary::onInitDilaog(wxInitDialogEvent& event) {
/////////////////////////////////////////////////////////////
	m_tsSummary->SetColumnWidth(0, 120);
	m_tsSummary->SetColumnWidth(1, 200);
	
	// fixing layout problems
	Update();
}
/////////////////////////////////////////////////////////////
void CncLastProcessingTimestampSummary::addTimestamp(const wxString& key, const wxString& val) {
/////////////////////////////////////////////////////////////
	unsigned int index = m_tsSummary->GetItemCount();
	
	m_tsSummary->InsertItem(index, "",  0);
	m_tsSummary->SetItem(index, 0, key);
	m_tsSummary->SetItem(index, 1, val);
}
/////////////////////////////////////////////////////////////
void CncLastProcessingTimestampSummary::onClose(wxCommandEvent& event) {
/////////////////////////////////////////////////////////////
	Show(false);
}
