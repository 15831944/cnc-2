#include "SecureRun.h"

///////////////////////////////////////////////////////////////////
SecureRun::SecureRun(MainFrame* parent)
: SecureRunBase(parent)
, parentFrame(parent)
///////////////////////////////////////////////////////////////////
{
	updateButtonState();
}
///////////////////////////////////////////////////////////////////
SecureRun::~SecureRun() {
///////////////////////////////////////////////////////////////////
}
///////////////////////////////////////////////////////////////////
void SecureRun::updateButtonState() {
///////////////////////////////////////////////////////////////////
	return;
	m_btRun->Enable(parentFrame->GetRcRun()->IsEnabled());
	m_btPause->Enable(parentFrame->GetRcPause()->IsEnabled());
}
///////////////////////////////////////////////////////////////////
void SecureRun::pause(wxCommandEvent& event) {
///////////////////////////////////////////////////////////////////
	wxCommandEvent evt(wxEVT_COMMAND_BUTTON_CLICKED);
	wxPostEvent(parentFrame->GetRcPause(), evt);
	updateButtonState();
}
///////////////////////////////////////////////////////////////////
void SecureRun::stop(wxCommandEvent& event) {
///////////////////////////////////////////////////////////////////
	wxCommandEvent evt(wxEVT_COMMAND_BUTTON_CLICKED);
	wxPostEvent(parentFrame->GetRcStop(), evt);
	updateButtonState();
}
///////////////////////////////////////////////////////////////////
void SecureRun::run(wxCommandEvent& event) {
///////////////////////////////////////////////////////////////////
	parentFrame->secureRun();
	updateButtonState();
	Show(false);
}
