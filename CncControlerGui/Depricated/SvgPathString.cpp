#include "SvgPathString.h"

////////////////////////////////////////////////////////////////////////////
void SvgPathString::initPath() {
////////////////////////////////////////////////////////////////////////////
	pathStr.clear();
}
////////////////////////////////////////////////////////////////////////////
void SvgPathString::finishPath() {
	if ( pathStr.length() > 0 ) {
		pathStr.Prepend("<path d=\"");
		pathStr.Append(wxString::Format("\" %s\n />\n", getDefaultSvgElementEnd()));
	}
}
////////////////////////////////////////////////////////////////////////////
const wxString& SvgPathString::get() {
////////////////////////////////////////////////////////////////////////////
	finishPath();
	static wxString s;
	s = pathStr;
	
	initPath();
	return s;
}
////////////////////////////////////////////////////////////////////////////
void SvgPathString::add(const wxString& pf) {
	if ( pf.length() > 0 )
		pathStr << pf << ' ';
}
////////////////////////////////////////////////////////////////////////////
bool SvgPathString::addPath(const wxString& p) {
////////////////////////////////////////////////////////////////////////////
	add(wxString::Format(" %s", p));
	return true;
}
